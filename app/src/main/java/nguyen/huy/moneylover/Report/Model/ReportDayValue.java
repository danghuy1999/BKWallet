package nguyen.huy.moneylover.Report.Model;

public class ReportDayValue {
    private String day;
    private Long value;

    public ReportDayValue() {
    }

    public String getDay() {
        return day;
    }

    public void setDay(String day) {
        this.day = day;
    }

    public Long getValue() {
        return value;
    }

    public void setValue(Long value) {
        this.value = value;
    }
}
