package nguyen.huy.moneylover.Report;

public class ReportDayValue {
    private String day;
    private Long value;

    ReportDayValue() {
    }

    String getDay() {
        return day;
    }

    void setDay(String day) {
        this.day = day;
    }

    public Long getValue() {
        return value;
    }

    public void setValue(Long value) {
        this.value = value;
    }
}
